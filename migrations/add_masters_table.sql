-- ============================================================================
-- МИГРАЦИЯ: Добавление таблицы masters и связи с orders
-- ============================================================================
-- Описание: 
--   Создаётся таблица мастеров, добавляется внешний ключ в таблицу заказов,
--   создаются индексы для оптимизации запросов.
-- 
-- Связи:
--   Один мастер может иметь много заказов (one-to-many)
-- ============================================================================

-- ============================================================================
-- ЧАСТЬ 1: Создание таблицы masters
-- ============================================================================

CREATE TABLE masters (
    -- Первичный ключ (автоинкремент)
    id SERIAL PRIMARY KEY,
    
    -- ФИО мастера (обязательное поле)
    name VARCHAR(100) NOT NULL,
    
    -- Специализация (слесарь, электрик, маляр, кузовщик и т.д.)
    specialization VARCHAR(50) NOT NULL,
    
    -- Контактный телефон (опционально)
    phone VARCHAR(20),
    
    -- Дата найма (по умолчанию — текущее время)
    hire_date TIMESTAMP NOT NULL DEFAULT NOW()
);

-- ============================================================================
-- ЧАСТЬ 2: Создание индексов для masters
-- ============================================================================

-- Индекс на specialization (для быстрого поиска мастеров по специализации)
CREATE INDEX idx_masters_specialization ON masters(specialization);

-- Индекс на id (обычно автоматический, но для явности)
CREATE INDEX idx_masters_id ON masters(id);

-- ============================================================================
-- ЧАСТЬ 3: Добавление внешнего ключа в таблицу orders
-- ============================================================================

-- Добавляем колонку master_id (может быть NULL, если заказ ещё не назначен мастеру)
ALTER TABLE orders 
ADD COLUMN master_id INTEGER;

-- Добавляем ограничение внешнего ключа
-- ON DELETE SET NULL: если мастера удалят из БД, у заказов master_id станет NULL
ALTER TABLE orders 
ADD CONSTRAINT fk_orders_master_id 
    FOREIGN KEY (master_id) 
    REFERENCES masters(id) 
    ON DELETE SET NULL;

-- ============================================================================
-- ЧАСТЬ 4: Создание индексов для orders
-- ============================================================================

-- Индекс на master_id (ускоряет JOIN orders с masters)
CREATE INDEX idx_orders_master_id ON orders(master_id);

-- Индекс на status (часто фильтруют заказы по статусу: "принят", "в работе", "готов")
CREATE INDEX idx_orders_status ON orders(status);

